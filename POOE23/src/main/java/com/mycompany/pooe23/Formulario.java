    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double A, B, C;
    double discriminante, raiz1, raiz2;

    A = Double.parseDouble(txtA.getText());
    B = Double.parseDouble(txtB.getText());
    C = Double.parseDouble(txtC.getText());

    discriminante = B * B - 4 * A * C;

    if (discriminante > 0) {
        raiz1 = (-B + Math.sqrt(discriminante)) / (2 * A);
        raiz2 = (-B - Math.sqrt(discriminante)) / (2 * A);

        txtSolucion1.setText(String.valueOf(raiz1));
        txtSolucion2.setText(String.valueOf(raiz2));
    } else if (discriminante == 0) {
        raiz1 = -B / (2 * A);

        txtSolucion1.setText(String.valueOf(raiz1));
        txtSolucion2.setText("No hay otra soluci√≥n");
    } else {
        txtSolucion1.setText("No hay soluciones reales");
        txtSolucion2.setText("");
    }
    }//GEN-LAST:event_btnCalcularActionPerformed

